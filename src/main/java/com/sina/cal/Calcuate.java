public class Calcuate {

    public int add(int a, int b){
        return a+b;
    }

    public int minus(int a, int b){
        return a-b;
    }

    public int divide(int a, int b){
        return a/b;
    }

    public int mull(int a, int b){
        return a*b;
    }
}
